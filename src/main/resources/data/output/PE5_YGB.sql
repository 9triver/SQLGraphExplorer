    LEDGERREPORT.ID,
    LEDGERREPORT.LEDGERREPORTTEMPLATE_ID,
    LEDGERREPORT.INSTITUTION_ID,
    LEDGERREPORT.TERM
FROM
    LEDGERREPORT
WHERE
    (LEDGERREPORT.ID = TEMP_TABLE_0.ID)
    AND (LEDGERREPORT.TERM = TEMP_TABLE_0.TERM)
    AND (LEDGERREPORT.TERM = V_DATE)
    AND (LEDGERREPORT.TERM = V_DATE);

