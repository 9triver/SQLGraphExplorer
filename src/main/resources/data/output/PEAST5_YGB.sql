OR REPLACE PROCEDURE INVERSE_EAST51_YGB_GL(PARA_NBJGH IN VARCHAR2(255)) IS BEGIN
SELECT
    EAST51_YGB_GL.JGH
FROM
    EAST51_YGB_GL
WHERE
    (EAST51_YGB_GL.JGH = PARA_NBJGH)
    AND TRUE;

END INVERSE_EAST51_YGB_GL;

