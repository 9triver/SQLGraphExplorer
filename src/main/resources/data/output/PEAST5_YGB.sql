CREATE
OR REPLACE PROCEDURE INVERSE_EAST51_YGB(
    PARA_YGZT IN VARCHAR2(255),
    PARA_PFRQ IN VARCHAR2(255),
    PARA_YGLX IN VARCHAR2(255),
    PARA_LXDH IN VARCHAR2(255),
    PARA_GH IN VARCHAR2(255),
    PARA_GWBH IN VARCHAR2(255),
    PARA_GWMC IN VARCHAR2(255),
    PARA_GJ IN VARCHAR2(255),
    PARA_ZJHM IN VARCHAR2(255),
    PARA_RZRQ IN VARCHAR2(255),
    PARA_XM IN VARCHAR2(255)
) IS BEGIN
SELECT
    EAST51_YGB.RZRQ,
    EAST51_YGB.BBZ,
    EAST51_YGB.ZJHM,
    EAST51_YGB.GH,
    EAST51_YGB.GJ,
    EAST51_YGB.YGLX,
    EAST51_YGB.XM,
    EAST51_YGB.YGZT,
    EAST51_YGB.GWBH,
    EAST51_YGB.GWMC,
    EAST51_YGB.LXDH,
    EAST51_YGB.PFRQ
FROM
    EAST51_YGB
WHERE
    (EAST51_YGB.RZRQ = PARA_RZRQ)
    AND (EAST51_YGB.ZJHM = PARA_ZJHM)
    AND (EAST51_YGB.GH = PARA_GH)
    AND (EAST51_YGB.GJ = PARA_GJ)
    AND (EAST51_YGB.YGLX = PARA_YGLX)
    AND (EAST51_YGB.XM = PARA_XM)
    AND (EAST51_YGB.YGZT = PARA_YGZT)
    AND (EAST51_YGB.GWBH = PARA_GWBH)
    AND (EAST51_YGB.GWMC = PARA_GWMC)
    AND (EAST51_YGB.LXDH = PARA_LXDH)
    AND (EAST51_YGB.PFRQ = PARA_PFRQ)
    AND EAST51_YGB.XM;

END INVERSE_EAST51_YGB;

