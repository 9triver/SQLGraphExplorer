    SELECT
      L.ID ID,
      T.TABLENAME TABLE_NAME,
      I.INSTITUTIONID INSTITUTIONID,
      L.TERM TERM
    FROM
      LEDGERREPORT L
      LEFT JOIN REPORTTEMPLATE T ON L.LEDGERREPORTTEMPLATE_ID = T.ID
      LEFT JOIN INSTITUTION I ON L.INSTITUTION_ID = I.ID
